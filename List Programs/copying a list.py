original_list = [1, 2, 3, 4, 5]

# Using copy() method
copied_list_method = original_list.copy()

# Using slicing
copied_list_slicing = original_list[:]

print("Original List:", original_list)
print("Copied List (using copy() method):", copied_list_method)
print("Copied List (using slicing):", copied_list_slicing)
