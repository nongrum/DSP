def find_sum_of_elements(lst):
    """
    Find the sum of elements in the given list.
    """
    sum_elements = sum(lst)
    return sum_elements

# Example Usage:
my_list = [1, 2, 3, 4, 5]

result = find_sum_of_elements(my_list)

print(f"List: {my_list}")
print(f"Sum of elements in the list: {result}")
